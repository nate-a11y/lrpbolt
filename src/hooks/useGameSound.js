const STORAGE_KEY = "lrp_game_sound";
const listeners = new Set();
let sharedEnabled;
let sharedSounds;

const readStoredPreference = () => {
  if (typeof window === "undefined") {
    return true;
  }
  try {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    return stored ? JSON.parse(stored) : true;
  } catch (error) {
    console.warn("Sound preference read failed", error);
    return true;
  }
};

const persistPreference = (value) => {
  if (typeof window === "undefined") {
    return;
  }
  try {
    window.localStorage.setItem(STORAGE_KEY, JSON.stringify(value));
  } catch (error) {
    console.warn("Sound pref persist failed", error);
  }
};

const ensureInitialized = () => {
  if (typeof sharedEnabled === "undefined") {
    sharedEnabled = readStoredPreference();
  }
};

const getSharedSounds = () => {
  if (sharedSounds) {
    return sharedSounds;
  }
  if (typeof Audio === "undefined") {
    sharedSounds = {};
    return sharedSounds;
  }
  sharedSounds = Object.entries(SOUND_SPECS).reduce((acc, [name, spec]) => {
    if (!spec?.src) {
      return acc;
    }
    try {
      const audio = new Audio(spec.src);
      if (typeof spec.volume === "number") {
        audio.volume = spec.volume;
      }
      acc[name] = audio;
    } catch (error) {
      console.warn("Sound init failed", error);
    }
    return acc;
  }, {});
  return sharedSounds;
};

export default function useGameSound() {
  ensureInitialized();
  const [enabled, setEnabledState] = useState(() =>
    typeof sharedEnabled === "undefined" ? true : sharedEnabled,
  );

  useEffect(() => {
    const notify = (value) => {
      setEnabledState(value);
    };
    listeners.add(notify);
    return () => {
      listeners.delete(notify);
    };
  }, []);

  const sounds = useMemo(() => getSharedSounds(), []);

  const setEnabled = useCallback((value) => {
    const next = Boolean(value);
    setEnabledState(next);
    if (sharedEnabled === next) {
      persistPreference(next);
      return;
    }
    sharedEnabled = next;
    persistPreference(next);
    listeners.forEach((listener) => {
      try {
        listener(next);
      } catch (error) {
        console.warn("Sound listener error", error);
      }
    });
  }, []);

  const play = useCallback(
    (name) => {
      if (!enabled) {
        return;
      }
      const sound = sounds[name];
      if (sound) {
        try {
          sound.currentTime = 0;
          const playPromise = sound.play();
          if (playPromise && typeof playPromise.catch === "function") {
            playPromise.catch((error) => {
              console.warn("Sound playback failed", error);
            });
          }
        } catch (error) {
          console.warn("Play failed", error);
        }
      }
    },
    [enabled, sounds],
  );

  return { enabled, setEnabled, play };
}
